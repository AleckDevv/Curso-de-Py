"""
Operadores:

+ -> soma
- -> subtração
* -> multiplicação
/ -> divisão
// -> divisão inteira
% -> resto da divisão
** -> exponenciação
"""

# soma
""" 
a = 10
b = 18
soma = a + b
print(soma) 
"""

# subtração
""" 
a = 10
b = 5
subtracao = a - b
print(subtracao)
"""

# multiplicação
""" 
a = 10
b = 10
multiplicacao = a * b
print(f"Mulplicação {multiplicacao}")
 """

# Divisão
""" 
a = 10
b = 10
divisao = a / b
print(f"Divisão: {divisao}") 
"""

# Divisão inteira
""" 
a = 10
b = 10
divisao_inteira = a // b
print(f"Divisão inteira: {divisao_inteira}") """

# Resto da divisão
""" 
a = 10
b = 10
resto = a % b
print(f"Resto: {resto}") 
"""


# Exponenciação
""" 
a = 10
b = 10
exponenciacao = a ** b
print(f"Ao quadrado: {exponenciacao}") 
"""

